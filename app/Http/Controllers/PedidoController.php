    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $pizza = Pizza::find($request->pizza);
        $cliente = Cliente::find($request->cliente);
        //var_dump($cliente);die;
        if(isset($pizza) && isset($cliente) && isset($request->endereco)){
            $pedido = new Pedido();
            $pedido->cliente_id = $cliente->id;
            $pedido->pizza_id = $pizza->id;
            $pedido->endereco = $request->endereco;

            $pedido->save();

            return response($pedido,200);
        }
        return response("Erro ao gerar Pedido",500);
    }

